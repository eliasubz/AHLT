#!/usr/bin/env python3

import argparse
import csv
import os

import matplotlib.pyplot as plt


def parse_args():
    parser = argparse.ArgumentParser(
        description="Plot top-k mutual information feature scores from a TSV file."
    )
    parser.add_argument("mi_tsv", help="Path to MI TSV file generated by feature_mi.py")
    parser.add_argument(
        "--topk",
        type=int,
        default=200,
        help="Number of top-ranked features to plot (default: 200)",
    )
    parser.add_argument(
        "--output",
        default=None,
        help="Output image path. Defaults to <mi_tsv>.top<k>.png",
    )
    parser.add_argument(
        "--title",
        default=None,
        help="Custom plot title",
    )
    return parser.parse_args()


def read_topk(mi_tsv, topk):
    rows = []
    total_features = 0
    with open(mi_tsv, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for row in reader:
            total_features += 1
            if len(rows) < topk:
                rows.append((int(row["rank"]), row["feature"], float(row["mi"])))
    return rows, total_features


def main():
    args = parse_args()
    rows, total_features = read_topk(args.mi_tsv, args.topk)
    if not rows:
        raise ValueError(f"No rows found in MI file: {args.mi_tsv}")

    ranks = [r[0] for r in rows]
    features = [r[1] for r in rows]
    mi_scores = [r[2] for r in rows]

    # Reverse for horizontal bars so rank 1 appears at top.
    features = features[::-1]
    mi_scores = mi_scores[::-1]

    fig_h = max(6, 0.32 * len(features) + 1.5)
    plt.figure(figsize=(12, fig_h))
    bars = plt.barh(features, mi_scores)
    plt.xlabel("Mutual Information")
    plt.ylabel("Feature")
    plt.title(args.title or f"Top {len(features)} / {total_features} Features by MI")

    # Add compact value labels at bar ends.
    for b, v in zip(bars, mi_scores):
        plt.text(v, b.get_y() + b.get_height() / 2, f" {v:.4f}", va="center", fontsize=8)

    plt.tight_layout()

    out_path = args.output or f"{args.mi_tsv}.top{len(rows)}.png"
    out_dir = os.path.dirname(out_path)
    if out_dir:
        os.makedirs(out_dir, exist_ok=True)
    plt.savefig(out_path, dpi=220)
    print(f"Saved plot to: {out_path}")
    print(f"Total features in file: {total_features}")


if __name__ == "__main__":
    main()
